const XLSX = require("xlsx");
const path = require("path");
const fs = require("fs");

const PROJECT_ROOT = path.join(__dirname, "..");
const EXCEL_PATH = path.join(PROJECT_ROOT, "Project_Info", "Material_Comparison.xlsx");
const OUTPUT_PATH = path.join(PROJECT_ROOT, "src", "data", "material-comparison.ts");

function main() {
  const workbook = XLSX.readFile(EXCEL_PATH);
  const sheet = workbook.Sheets[workbook.SheetNames[0]];
  const rows = XLSX.utils.sheet_to_json(sheet, { header: 1, defval: "" });

  if (rows.length < 2) {
    throw new Error("Material_Comparison.xlsx must include a header row and at least one data row.");
  }

  const [header, ...dataRows] = rows;
  const attributeLabel = String(header[0] || "Material").trim();
  const materials = header.slice(1).map((name) => String(name).trim()).filter(Boolean);

  const comparisonRows = dataRows
    .map((row) => {
      const attribute = String(row[0] || "").trim();
      if (!attribute) return null;

      const values = {};
      materials.forEach((material, index) => {
        values[material] = String(row[index + 1] ?? "").trim();
      });

      return { attribute, values };
    })
    .filter(Boolean);

  const output = `// Generated by scripts/generate-material-comparison.js — do not edit by hand.

export type MaterialComparisonRow = {
  attribute: string;
  values: Record<string, string>;
};

export const MATERIAL_COMPARISON_ATTRIBUTE_LABEL = ${JSON.stringify(attributeLabel)};

export const MATERIAL_COMPARISON_COLUMNS = ${JSON.stringify(materials, null, 2)} as const;

export const MATERIAL_COMPARISON_ROWS: MaterialComparisonRow[] = ${JSON.stringify(comparisonRows, null, 2)};
`;

  fs.writeFileSync(OUTPUT_PATH, output, "utf8");
  console.log(`Wrote ${comparisonRows.length} rows to ${path.relative(PROJECT_ROOT, OUTPUT_PATH)}`);
}

main();
